saldo = 500
saldo += 100  # saldo = saldo + 100
print(saldo)  # 600
saldo -= 50  # saldo = saldo - 50
print(saldo)  # 550
saldo *= 2  # saldo = saldo * 2
print(saldo)  # 1100
saldo /= 4  # saldo = saldo / 4
print(saldo)  # 275.0
saldo //= 3  # saldo = saldo // 3
print(saldo)  # 91.0
saldo %= 7  # saldo = saldo % 7
print(saldo)  # 0.0
saldo **= 3  # saldo = saldo ** 3
print(saldo)  # 0.0
